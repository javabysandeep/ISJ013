package _3_operators._6_relational_operators;

public class Demo3 {
    public static void main(String[] args) {
        System.out.println(10 % 2 == 0);//true
        System.out.println(10 % 3 == 0);//false
        System.out.println(10 % 4 == 0);//false
        System.out.println(10 % 5 == 0);//false

        System.out.println(2025 % 400 == 0);//false
        System.out.println(2025 % 4 == 0);//false
        System.out.println(2025 % 100 == 0);//false

    }
}
