class Variable1 {

	public static void main(String[] args){
			
			byte byteValue = 127;
			System.out.println("Byte value is "+ byteValue);
			System.out.println("Byte Range is from "+Byte.MIN_VALUE+" to "+Byte.MAX_VALUE);
	
			
			short shortValue = 32767;
			System.out.println("Short value is "+ shortValue);
			System.out.println("Short Range is from "+Short.MIN_VALUE+" to "+Short.MAX_VALUE);
	
			int intValue = 2147483647;
			System.out.println("int value is "+ intValue);
			System.out.println("int Range is from "+Integer.MIN_VALUE+" to "+Integer.MAX_VALUE);
	
			long longValue = 99999999L;
			System.out.println("long value is "+ longValue);
			System.out.println("long Range is from "+Long.MIN_VALUE+" to "+Long.MAX_VALUE);
	
			float floatValue = 110.55F;
			System.out.println("Float value is "+ floatValue);
			System.out.println("float Range is from "+Float.MIN_VALUE+" to "+Float.MAX_VALUE);
	
			double doubleValue = 10000010.55D;
			System.out.println("Double value is "+ doubleValue);
			System.out.println("double Range is from "+Double.MIN_VALUE+" to "+Double.MAX_VALUE);
	
			
			
			char charValue = 'A';
			System.out.println("Char value is "+ charValue);
			System.out.println("char Range is from "+(int)Character.MIN_VALUE+" to "+(int)Character.MAX_VALUE);
			System.out.println("char Range is from "+Character.MIN_VALUE+" to "+Character.MAX_VALUE);
		
			char charValue2 = 66;
			System.out.println("Char value is "+ charValue2);
			
			
			boolean booleanValue = false;
			System.out.println("boolean value is "+ booleanValue);
			
			
	
	}

}