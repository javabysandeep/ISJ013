package _6_class_components._1_variables;

public class Demo1 {
    public static void main(String[] args) {
       //a : local primitive variable
        int a;//variable declaration
        //System.out.println(a);//CTE

        a = 100;//variable initialization
        System.out.println(a);

        int b = 200; //declaration + initialization
        System.out.println(b);

        //local non-primitive or local reference variable
        String str = "abc";
        System.out.println(str);

    }
}
