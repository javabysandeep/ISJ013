package _4_control_statements._1_if_else;

public class Demo4 {
    public static void main(String[] args) {
        int number = 12;

        if (number % 2 == 0) {
            System.out.println(number + " is an even number");
        }

        if (number % 2 != 0) {
            System.out.println(number + " is an odd number");
        }
    }
}
