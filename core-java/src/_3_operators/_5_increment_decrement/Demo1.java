package _3_operators._5_increment_decrement;

public class Demo1 {
    public static void main(String[] args) {
        int a = 1;
        int b = ++a; // a = 2, b = 2
        System.out.println("a = " + a);//2
        System.out.println("b = " + b);//2


        int c = 5;
        int d = --c; // c = 4, d = 4
        System.out.println("c = " + c);//4
        System.out.println("d = " + d);//4
    }
}
